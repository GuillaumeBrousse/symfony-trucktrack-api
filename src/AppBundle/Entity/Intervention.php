<?php

namespace AppBundle\Entity;

/**
 * Intervention
 */
class Intervention
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var bool
     */
    private $state;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set state
     *
     * @param boolean $state
     *
     * @return Intervention
     */
    public function setState($state)
    {
        $this->state = $state;

        return $this;
    }

    /**
     * Get state
     *
     * @return bool
     */
    public function getState()
    {
        return $this->state;
    }
    /**
     * @var \AppBundle\Entity\Incident
     */
    private $incident;

    /**
     * @var \AppBundle\Entity\SuiviMission
     */
    private $suiviMission;

    /**
     * @var \AppBundle\Entity\Technicien
     */
    private $technicien;

    /**
     * @var \AppBundle\Entity\Vehicule
     */
    private $vehicule;


    /**
     * Set incident
     *
     * @param \AppBundle\Entity\Incident $incident
     *
     * @return Intervention
     */
    public function setIncident(\AppBundle\Entity\Incident $incident)
    {
        $this->incident = $incident;

        return $this;
    }

    /**
     * Get incident
     *
     * @return \AppBundle\Entity\Incident
     */
    public function getIncident()
    {
        return $this->incident;
    }

    /**
     * Set suiviMission
     *
     * @param \AppBundle\Entity\SuiviMission $suiviMission
     *
     * @return Intervention
     */
    public function setSuiviMission(\AppBundle\Entity\SuiviMission $suiviMission)
    {
        $this->suiviMission = $suiviMission;

        return $this;
    }

    /**
     * Get suiviMission
     *
     * @return \AppBundle\Entity\SuiviMission
     */
    public function getSuiviMission()
    {
        return $this->suiviMission;
    }

    /**
     * Set technicien
     *
     * @param \AppBundle\Entity\Technicien $technicien
     *
     * @return Intervention
     */
    public function setTechnicien(\AppBundle\Entity\Technicien $technicien)
    {
        $this->technicien = $technicien;

        return $this;
    }

    /**
     * Get technicien
     *
     * @return \AppBundle\Entity\Technicien
     */
    public function getTechnicien()
    {
        return $this->technicien;
    }

    /**
     * Set vehicule
     *
     * @param \AppBundle\Entity\Vehicule $vehicule
     *
     * @return Intervention
     */
    public function setVehicule(\AppBundle\Entity\Vehicule $vehicule)
    {
        $this->vehicule = $vehicule;

        return $this;
    }

    /**
     * Get vehicule
     *
     * @return \AppBundle\Entity\Vehicule
     */
    public function getVehicule()
    {
        return $this->vehicule;
    }
}
